    // Método para cargar rutinas
    void cargarRutinas(List<Ejercicio> ejerciciosSeleccionados) {
        // Crear una lista de nombres de ejercicios a partir de los objetos Ejercicio
        List<String> nombresEjercicios = ejerciciosSeleccionados.stream()
                                                               .map(Ejercicio::getNombre)
                                                               .collect(Collectors.toList());

        // Actualizar el JList con los nombres de los ejercicios seleccionados
        listaEjerciciosSeleccionados.setListData(nombresEjercicios.toArray(new String[0]));
    }

    

