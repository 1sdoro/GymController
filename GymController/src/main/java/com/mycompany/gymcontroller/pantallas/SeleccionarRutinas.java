    private void btnAsignarRutinasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAsignarRutinasActionPerformed
        // Obtener los ejercicios seleccionados de la lista
    List<String> ejerciciosAsignados = listaEjerciciosSeleccionados.getSelectedValuesList();

    if (ejerciciosAsignados.isEmpty()) {
        System.out.println("No hay ejercicios seleccionados para asignar.");
    } else {
        String usuarioNombre = "nombreDelUsuario";  // Aquí deberías obtener el nombre de usuario de forma dinámica

        // Asignar cada ejercicio seleccionado al usuario
        UsuarioController usuarioController = new UsuarioController();
        for (String nombreEjercicio : ejerciciosAsignados) {
            Rutina rutina = new Rutina(nombreEjercicio);  // Crear una rutina para el ejercicio seleccionado
            usuarioController.asignarRutinaAUsuario(usuarioNombre, rutina);
            System.out.println("Ejercicio asignado al usuario: " + nombreEjercicio);
        }
        System.out.println("Rutinas asignadas al usuario: " + usuarioNombre);
    }
    
    if (ejerciciosAsignados.isEmpty()) {
        System.out.println("No hay ejercicios seleccionados para asignar.");
    } else {
        String usuarioNombre = "nombreDelUsuario";  // Obtén el nombre del usuario de manera dinámica

        UsuarioController usuarioController = new UsuarioController();
        for (String nombreEjercicio : ejerciciosAsignados) {
            Rutina rutina = new Rutina(nombreEjercicio);
            usuarioController.asignarRutinaAUsuario(usuarioNombre, rutina);
            System.out.println("Ejercicio asignado al usuario: " + nombreEjercicio);
        }

        // Mostrar mensaje de éxito
        JOptionPane.showMessageDialog(this, "Rutinas asignadas correctamente al usuario " + usuarioNombre, "Éxito", JOptionPane.INFORMATION_MESSAGE);
    }
    }//GEN-LAST:event_btnAsignarRutinasActionPerformed

