package com.mycompany.gymcontroller.modelo;

/**
 *
 * @author Dorothea Ramirez
 */
public class Administradores 
{
    private  String Administrador;
    private  String nombreAdm;
    private  String contrasennaAdm;

    public Administradores(String nombreAdm, String contrasennaAdm) {
        this.Administrador = Administrador;
        this.nombreAdm = nombreAdm;
        this.contrasennaAdm = contrasennaAdm;
    }
    
    public String getAdministrador() {
        return Administrador;
    }

    public void setAdministrador(String Administrador) {
        this.Administrador = Administrador;
    }

    public String getNombreAdm() {
        return nombreAdm;
    }

    public void setNombreAdm(String nombreAdm) {
        this.nombreAdm = nombreAdm;
    }

    public String getContrasennaAdm() {
        return contrasennaAdm;
    }

    public void setContrasennaAdm(String contrasennaAdm) {
        this.contrasennaAdm = contrasennaAdm;
    }

    
    
}
